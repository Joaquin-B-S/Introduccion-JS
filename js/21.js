// Arrow functions

const suma = (numero1, numero2) => {
    return numero1 + numero2;
};
// Llamada a la funcion
const resultado = suma(10, 20); // parametros
console.log(resultado); // 30
// Arrow functions con un solo argumento
const doble = (numero) => {
    return numero * 2;
};
// Llamada a la funcion
const resultadoDoble = doble(5); // parametros
console.log(resultadoDoble); // 10
