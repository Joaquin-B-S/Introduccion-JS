// Objeto Math
// Math es un objeto global que proporciona propiedades y métodos para realizar operaciones matemáticas
// Propiedades
// Math.PI: Proporciona el valor de pi
console.log(Math.PI); // 3.141592653589793
// Math.E: Proporciona el valor de e
console.log(Math.E); // 2.718281828459045
// Math.LN2: Proporciona el logaritmo natural de 2
console.log(Math.LN2); // 0.6931471805599453
// Math.LN10: Proporciona el logaritmo natural de 10
console.log(Math.LN10); // 2.302585092994046
// Math.LOG2E: Proporciona el logaritmo en base 2 de e
console.log(Math.LOG2E); // 1.4426950408889634
console.log(Math.random()); // 0.1234567890123456
console.log(Math.round(4.5)); // 5
console.log(Math.round(4.4)); // 4
console.log(Math.floor(4.5)); // 4
console.log(Math.floor(4.4)); // 4
console.log(Math.ceil(4.5)); // 5
console.log(Math.ceil(4.4)); // 5
console.log(Math.abs(-4.5)); // 4.5
console.log(Math.abs(4.5)); // 4.5
console.log(Math.sqrt(16)); // 4
console.log(Math.sqrt(-16)); // NaN
console.log(Math.pow(2, 3)); // 8
console.log(Math.pow(2, -3)); // 0.125
console.log(Math.max(1, 2, 3)); // 3
console.log(Math.min(1, 2, 3)); // 1
